#비교 연산자:== 같다 !=다르다 >크다 <작다 >=크거나 같다 <=작거나 같다
#문자 비교:가나다 순으로 앞에 있는것이 더 작은 값이다.
print('가방'=='가방')
print('가방'!='하마')
print('가방'>'하마')
print('가방'<'하마')


#범위 논리가 가능하다.
x=25
print(26<x<30)

#논리 연산자:and, or, not
print(not True) #False
print(True and True) #True
print(True and False) #False
print(True or False) #True
print(False or False) #False

